import struct

def write_sudoku_to_binary(filename, board):
    with open(filename, "wb") as file:
        file.write(struct.pack("B", 3))  # Write the base (3)
        for row in board:
            file.write(struct.pack("9B", *row))  # Write the board as bytes

sudoku_board = [
    [5, 3, 0, 0, 7, 0, 0, 0, 0],
    [6, 0, 0, 1, 9, 5, 0, 0, 0],
    [0, 9, 8, 0, 0, 0, 0, 6, 0],
    [8, 0, 0, 0, 6, 0, 0, 0, 3],
    [4, 0, 0, 8, 0, 3, 0, 0, 1],
    [7, 0, 0, 0, 2, 0, 0, 0, 6],
    [0, 6, 0, 0, 0, 0, 2, 8, 0],
    [0, 0, 0, 4, 1, 9, 0, 0, 5],
    [0, 0, 0, 0, 8, 0, 0, 7, 9]
] 

write_sudoku_to_binary("sudoku.dat", sudoku_board)